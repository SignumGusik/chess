final class RulesInteractor: RulesInteractorProtocol {
    func makeRulesText() -> String {
        """
        Шахматы

        Цель игры:
        Поставить мат королю соперника.

        Базовые правила:
        + Белые ходят первыми.
        + Король ходит на одну клетку в любую сторону.
        + Ферзь ходит по вертикали, горизонтали и диагонали.
        + Ладья ходит по вертикали и горизонтали.
        + Слон ходит по диагонали.
        + Конь ходит буквой Г.
        + Пешка ходит вперёд на одну клетку, а с начальной позиции может сходить на две клетки.
        + Пешка бьёт по диагонали.
        + В игре поддерживаются рокировка, взятие на проходе и превращение пешки.
        + Нельзя делать ход, после которого свой король остаётся под шахом.

        Шах, мат и пат:
        + Шах - это атака на короля.
        + Если игроку объявлен шах и он не может защититься, это мат.
        + Если у игрока нет допустимых ходов, но шаха нет, это пат.

        Превращение пешки:
        + Если пешка доходит до последней горизонтали, она превращается в одну из фигур:
          ферзя, ладью, слона или коня.

        Суперспособности:
        + Перед началом новой партии каждому игроку случайно предлагаются 3 способности.
        + Игрок выбирает 1 способность из 3.
        + Выбор выполняется по очереди в тайном режиме: сначала устройство передаётся белым, затем чёрным.
        + У каждого игрока в партии только одна выбранная суперспособность.

        Способности:
        1. Призыв фигуры
        + Один раз за игру можно призвать коня на пустую клетку своей половины доски.
        + Для белых доступна нижняя половина доски, для чёрных — верхняя.
        + Нельзя использовать способность, если после этого свой король окажется под шахом.

        2. Король как ферзь
        + Один раз за игру можно активировать специальный режим.
        + После активации следующим ходом король может сходить как ферзь.
        + После такого хода способность считается использованной.

        3. Баллистическая ракета
        + Один раз за игру можно уничтожить фигуру соперника.
        + Нельзя уничтожать короля и ферзя.
        + Нельзя использовать способность, если после этого свой король окажется под шахом.

        4. Усиление пешки
        + Способность становится доступной только после 40-го полного хода.
        + Один раз за игру можно превратить одну свою пешку в коня.
        + Нельзя использовать способность, если после этого свой король окажется под шахом.

        5. Машина времени
        + Если у одного из игроков выбрана эта способность, в партии становится доступен откат ходов кнопкой «Назад».
        + Откат возвращает игру к предыдущему сохранённому состоянию.

        Дополнительно:
        + Во время партии отображаются текущий ход, статус шаха/мата/пата и история ходов.
        + Игру можно поставить на паузу и продолжить позже из сохранения.
        """
    }
}
